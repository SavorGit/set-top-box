package com.savor.ads.bean;

public class BootVideo {
    /**开机视频地址*/
    private String url;
    /**开机视频md5值*/
    private String md5;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }
}
